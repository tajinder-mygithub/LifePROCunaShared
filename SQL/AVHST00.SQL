IF EXISTS(select * from INFORMATION_SCHEMA.COLUMNS 
where TABLE_NAME = 'PVHST' AND COLUMN_NAME='ROW_COLUMN')
ALTER TABLE PVHST DROP COLUMN
       ROW_COLUMN
;
--Drop Primary Key
IF EXISTS (SELECT CONSTRAINT_NAME FROM INFORMATION_SCHEMA.TABLE_CONSTRAINTS
           WHERE CONSTRAINT_NAME = 'KPVHST')
ALTER TABLE PVHST DROP CONSTRAINT KPVHST
;
IF EXISTS(select * from INFORMATION_SCHEMA.COLUMNS 
where TABLE_NAME = 'PVHST' AND COLUMN_NAME='VHST_KEY0')
ALTER TABLE PVHST DROP COLUMN
       VHST_KEY0
;
--Rebuild Primary Key
IF NOT EXISTS (SELECT CONSTRAINT_NAME FROM INFORMATION_SCHEMA.TABLE_CONSTRAINTS
           WHERE CONSTRAINT_NAME = 'KPVHST')
ALTER TABLE  PVHST
    ADD CONSTRAINT KPVHST             PRIMARY KEY (
    VCONVERT_PROGRAM,
    CLIENT_VERSION    )
;
--Drop Key Table
IF EXISTS (SELECT table_name FROM INFORMATION_SCHEMA.TABLES WHERE table_schema = 'dbo' AND table_name = 'PVHST_KEY1')
   DROP TABLE [dbo].[PVHST_KEY1]  