IF  EXISTS (SELECT * FROM sys.indexes WHERE object_id = OBJECT_ID(N'[dbo].[PCLMD]') AND name = N'PCLMD_X_3')
    DROP INDEX [PCLMD_X_3] ON [dbo].[PCLMD] WITH ( ONLINE = OFF )
;

CREATE NONCLUSTERED INDEX [PCLMD_X_3] ON [dbo].[PCLMD]
(
	[RECORD_STATUS] ASC,
	[PAYMENT_DATE] ASC
)
INCLUDE ( 	[COMPANY_CODE],
	[CLAIM_NUMBER],
	[DETAIL_SEQ],
	[PAYMENT_SEQ],
	[ADJ_SEQ],
	[PAYEE_NAME_ID],
	[POLICY_NUMBER])
;
